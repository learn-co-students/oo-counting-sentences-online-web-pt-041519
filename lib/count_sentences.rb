require 'pry'

class String

  def sentence?
    self.end_with?(".") ? true : false
  end

  def question?
    self.end_with?("?") ? true : false
  end

  def exclamation?
    self.end_with?("!") ? true : false
  end

  def count_sentences
    array_ct = self.split(/[.?!]/).reject { |string| string.empty? }
    array_ct.length
  end


end
